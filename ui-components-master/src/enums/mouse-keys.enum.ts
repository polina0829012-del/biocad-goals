/**
 * Ключи мыши.
 * @enum {string}
 * @member {string} MouseKeys.click - Клик.
 * @member {string} MouseKeys.mouseenter - Наведение мыши на объект.
 * @member {string} MouseKeys.mouseup - Отпускание кнопки мыши.
 * @member {string} MouseKeys.mousedown - Нажатие кнопки мыши.
 * @member {string} MouseKeys.mouseleave - Перемещение мыши вне объекта.
 */
export enum MouseKeys {
    click = 'click',
    mouseenter = 'mouseenter',
    mouseup = 'mouseup',
    mousedown = 'mousedown',
    mouseleave = 'mouseleave',
}
